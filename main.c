void TIMER16_1_IRQHandler(void)
{
	TMR_TMR16B1IR = TMR_TMR16B1IR_MR0;
	
	gpioSetValue(1,8,vline <= 12 && vline >= 11 ? 0 : 1);
	
	vline++;
	if(vline > 525) {
		vpix = 0;
		vline = 0;
	}
	if(vline < 45+vskip || (vpix >> 4) >= 24) return;
	
	uint16_t *p = vfb + (vpix >> 4) * 32;
	uint8_t *q = pattern + (((vpix >> 1) & 7) << 8);
	uint32_t w = 32;
	
	while(w--) {
		SSP_SSP0DR = q[*p++];
	}
	
	vpix++;
}

void PIOINT0_IRQHandler(void)
{
	static int d = 0, b = 0;
	if(b >= 1 && b < 9) d = (d >> 1) | (gpioGetValue(0,7) << 7);
	b++;
	if(b >= 11) {
		fifo32_put(&ps2_fifo,d);
		b = 0;
	}
	gpioIntClear(0,4);
}

